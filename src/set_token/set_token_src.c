#include "sh.h"

int	is_empty(char *str)
{
	if (str)
	{
		while (*str)
		{
			if (*str != ' ' && *str != '\n' && *str != '\t')
				return (0);
			++str;
		}
	}
	ft_strdel(&g_sel.cmd);
	return (1);
}
